<?php 

	function somar($a, $b){
		$soma = $a + $b;
		return "O resultado da soma de $a + $b é $soma";
	}

	function diminuir($a, $b){
		$diminuicao = $a - $b;
		return "O resultado da diminuição de $a - $b é $diminuicao";
	}

	function multiplicar($a, $b){
		$multiplicacao = $a * $b;
		return "O resultado da multiplicação de $a x $b é $multiplicacao";
	}

	function dividir($a, $b){
		$divisao = $a / $b;
		return "O resultado da divisão de $a / $b é $divisao";
	}

?>